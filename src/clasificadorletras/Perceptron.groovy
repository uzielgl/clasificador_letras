    public void convertToBipolar(){
        for( ArrayList l : letters){
            def t;
            def mapped = [];
            for( int x = 0; x < l[0].size(); x++){
                if( l[0][x] == "#") t = 1;
                else if( l[0][x] == "@" ) t = 1;
                else if( l[0][x] == "." ) t = -1;
                else if( l[0][x] == "o" ) t = -1;
                mapped.add( t );
            }
            l[0] = mapped;
        }
    }
    
